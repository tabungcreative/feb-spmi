    E-SPMI FKSP UNSIQ
</h3>
<p class="">
    E-SPMI FKSP UNSIQ  merupakan sistem informasi  kegiatan SPMI FKSP UNSIQ yang dikelola oleh Unit Penjaminan Mutu Penelitan dan Pengabdian Masyarakat Fakultas (UP3MF) secara digital. E-SPMI FKSP UNSIQ ini mengakomodir  menu-menu terkait penjaminan mutu fakultas, yaitu :
</p>

<ol>
    <li>Dokumen Manual mutu PPEPP SPMI FKSP UNSIQ </li>
    <li> Dokumen Sistem Operasional Prosedur (SOP) SPMI FKSP UNSIQ</li>
    <li>Dokumen Instruksi Kerja (IK) SPMI FKSP UNSIQ </li>
    <li>Dokumen Audit Mutu Internal SPMI FKSP UNSIQ </li>
    <li>Hasil Evaluasi Dosen oleh mahasiswa</li>
    <li>Laporan hasil penelitian dosen,</li>
    <li>Laporan hasil pengabdian dosen</li>
    <li>Informasi aktual kegiatan akademik FKSP</li>
    <li>Dokumentasi kegiatan akademik FKSP</li>
</ol>
